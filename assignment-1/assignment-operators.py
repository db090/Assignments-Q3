                                         #Assignment Operators
#Assign : assigns a value
a = 10
#Add & Assign : Adds and assigns
a += 5
#Subtract & Assign : Subtracts and assigns
a -= 3
#Multiply & Assign : Multiplies and assigns
a *= 2
#Divide & Assign : Divides and assigns	
a /= 4
#Floor Divide & Assign : Floor divides and assigns
a //= 2
#Modulus & Assign : Finds remainder and assigns
a%= 2
#Exponentiate & Assign : Raises power and assigns
a **= 3