                                            #Bitwise Operators
#Bitwise operators perform operations at the bit level on integers.
a=5 #0101
b=3 #0011
#AND : Sets bits to 1 if both bits are 1
a & b  
#OR : Sets bits to 1 if any bit is 1	
#XOR : Sets bits to 1 if bits are different
a ^ b  
#NOT : Inverts bits (flips 0s & 1s)
~a
#Left Shift : Shifts bits left (multiplies by 2)
a >= b  
#Right Shift : Shifts bits right (divides by 2)
a >> 1  
