                                         #Comparison Operators
#Comparison operators are used to compare two values and return a Boolean (True or False).
a=10
b=5
#Equal to : Checks if two values are equal
a == b  #output=False
#Not equal to : Checks if two values are not equal	
a != b  #output=True
#Greater than : checks if left value is greater
a > b  #output=True
#Less than : Checks if left value is smaller 
a < b  #output=False
#Greater than or equal to : Checks if left value is greater or equal
a >= b  #output=True
#Less than or equal to : Checks if left value is smaller or equal
a <= b  #output=False
