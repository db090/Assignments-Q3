                                         #Arithmetic Operators
x=2
y=4
#Addition : add two numbers
add=x+y  #output=8
#Subtraction : subtracts two numbers
sub=x-y  #output=-2
#Multiplication : multiplies two numbers
mul=x*y  #output=8
#Division : divides and returns a float 
div=x/y  #output=0.5
#Floor Division : returns the integer quotient
fdiv=x//y  #output=0
#Modulus : returns the remainder
mod=x%y  #output=2
#Exponentiation : returns the remainder
exp=x**y  #output=16
