                                                 #Membership Operators
#Membership operators are used to check whether a value exists in a sequence (like a list, tuple, string, etc.).
x=[1,2,3]
y="hello"
#in : Returns True if the value exists in the sequence
2 in x  #output=True
#not in : Returns True if the value does not exist in the sequence
"h" not in y  #output=False
